package org.legacyprojectx.datastructures;

public class TaggedSentences
{
	String[] Tokens;
	String[] tags;
	public String[] getTokens() {
		return Tokens;
	}
	public void setTokens(String[] tokens) {
		Tokens = tokens;
	}
	public String[] getTags() {
		return tags;
	}
	public void setTags(String[] tags) {
		this.tags = tags;
	}
	
}
